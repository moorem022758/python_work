# a number raised to the third power is called a cube. Fir example, the cuve of 2 is written
# as 2 ** 3 in python.
# Make a list of the first 10 cubes (that is, the cube of each integer from 1 through 10),
# and use a for loop to print the valus of each cute.
cubes = []
for cube in range(1, 10):
    cube1 = cube ** 3
    cubes.append(cube1)   
print(cubes)
    
    