# Use variable to represent a persons name include whitespace at beginning
# and end. Make sure to use each character combination "\t" and "\n" once
# print name once so whitespace is displayed
# print name using the three stripping functions lstrip(), rstrip(), and strip()
current_name = "\tMichael" "\t" "Moore\t\n"
print(current_name)
print(current_name.lstrip())
print(current_name.rstrip())
print(current_name.strip())